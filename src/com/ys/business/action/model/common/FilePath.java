package com.ys.business.action.model.common;

public class FilePath {
	
	private String save;

	private String view;
	
	private String web;	
		
	public String getSave() {
		return save;
	}
	
	public void setSave(String key) {
		this.save = key;
	}

	public String getView() {
		return view;
	}

	public void setView(String key) {
		this.view = key;
	}

	public String getWeb() {
		return web;
	}

	public void setWeb(String key) {
		this.web = key;
	}

}
